<template>
  <Chart chart-class="SecondarySplit" :options="options" />
</template>

<style lang="scss">
.SecondarySplit {
  text {
    fill: $grey-1;
  }
}
</style>

<script>
import { defineComponent } from "vue";
import { useI18n } from "vue-i18n";

import Chart from "./Chart";

export default defineComponent({
  name: "SecondarySplit",

  components: { Chart },

  props: {
    project: {
      required: true,
      type: Object
    }
  },

  setup(props, context) {
    const { t } = useI18n({ useScope: "global" });
    const names = {
      creator: props.project.creator.name,
      collaborator1: props.project.collaborators.find(
        c => c.id === "collaborator1"
      ).name,
      collaborator2: props.project.collaborators.find(
        c => c.id === "collaborator2"
      ).name,
      changeDaoCommunityTreasury: "ChangeDAO CommunityTreasury.eth",
      changeDaoOperationsTreasury: "ChangeDAO OperationsTreasury.eth"
    };

    for (let id in names) {
      names[id] = `${props.project.secondarySplit[id]}% â€“ ${names[id]}`;
    }

    const options = {
      donut: {
        width: 40,
        label: {
          show: false
        },
        title: t("Secondary Sales")
      },
      data: {
        type: "donut",
        json: props.project.secondarySplit,
        names,
        colors: {
          creator: "#2EBAD3",
          collaborator1: "#137C8E",
          collaborator2: "#005A6A",
          changeDaoCommunityTreasury: "#8001FB",
          changeDaoOperationsTreasury: "#4F009C"
        }
      },
      tooltip: {
        show: false
      },
      legend: {
        show: false
      }
    };

    return {
      options
    };
  }
});
</script>
